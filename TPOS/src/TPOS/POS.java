    private void jButon_DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButon_DeleteActionPerformed
     
        errorMessage();
    
    if (jTable1.getRowCount() == 0) {
        jLabelError.setText("No items to delete!");
        errorMessageTimer.start();
        return;
    }
    
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow == -1) {
        jLabelError.setText("Please select item to Delete!");
        errorMessageTimer.start();
        return;
    }
    
    // Get id (barcode) before potential deletion
    tb1 = (DefaultTableModel) jTable1.getModel(); // Ensure tb1 is set
    String idStr = tb1.getValueAt(selectedRow, 0).toString(); // Get barcode
    int id;
    try {
        id = Integer.parseInt(idStr); // Try to parse, assuming barcode is numeric
    } catch (NumberFormatException e) {
        jLabelError.setText("Invalid barcode format for deletion!");
        errorMessageTimer.start();
        return;
    }
    
    // Show confirmation dialog first
    int dialogResult = JOptionPane.showConfirmDialog(null, "Do you want to Delete product item from table?","Warning",JOptionPane.YES_NO_OPTION);
    
    if(dialogResult == JOptionPane.YES_OPTION){
        // Remove row from table
        tb1.removeRow(selectedRow);
        
        // Recalculate subtotal
        int del = 0;
        for(int i = 0; i < jTable1.getRowCount(); i++){
            del += Integer.parseInt(jTable1.getValueAt(i, 4).toString());
        }
        jTFSubT.setText(Integer.toString(del));
        
        // Database deletion (assuming this is needed, though per previous, should be deferred)
        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/TPOS", "root", "");
            String sql = "Delete from Sales WHERE id = ?";
            stm = conn.prepareStatement(sql);
            stm.setInt(1, id);
            stm.executeUpdate();

            jLabelSuccess.setText("Product Deleted!");
            errorMessageTimer.start();

            conn.close();
        } catch (SQLException ex) {
            Logger.getLogger(Product.class.getName()).log(Level.SEVERE, null, ex);
            jLabelError.setText("Database error during deletion: " + ex.getMessage());
            errorMessageTimer.start();
        }
    } else {
        jLabelError.setText("Deletion cancelled.");
        errorMessageTimer.start();
    }
        
    }//GEN-LAST:event_jButon_DeleteActionPerformed

    private void sales(){
    
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd");
        LocalDateTime now = LocalDateTime.now();
        String date = dtf.format(now);
        String cashier = jLableUname.getText();
        String subtot = jTFSubT.getText();
        String pay = jTFPay.getText();
        String balance = jTFBalance.getText();
        int lastinsertid = 0;
        
        
        if (cashier.isEmpty() || subtot.isEmpty() || pay.isEmpty()  || balance.isEmpty()) {
//            JOptionPane.showMessageDialog(null, "All fields are required!");
            jLabelError.setText("All fields are required!");
            errorMessageTimer.start();
            return;
        }
        
        
        try {
            conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/TPOS", "root", "");  //connects with the database
            String sql = "INSERT INTO Sales( Date,Cashier, Sub_Total, Pay, Balance) VALUES (?, ?, ?, ?,?)"; //inserts data into database
            insert = conn.prepareStatement(sql,Statement.RETURN_GENERATED_KEYS); //prepares a statement to run the query instead of compiling first 
            insert.setString(1, date); //set values
            insert.setString(2, cashier);
            insert.setString(3, subtot); //set values
            insert.setString(4, pay);
            insert.setString(5, balance);
            insert.executeUpdate(); // runs the sql statement
            //JOptionPane.showMessageDialog(null,"Sale added!");
            ResultSet genkeyres = insert.getGeneratedKeys();
            
            if(genkeyres.next()){
            
                lastinsertid = genkeyres.getInt(1);
            
            }else{
            
                JOptionPane.showMessageDialog(this, lastinsertid);
                
            }
            
            int rows = jTable1.getRowCount();
            
            String sql2 = "INSERT INTO sales_product(Sales_ID, Product_ID, Sell_Price, Qty, Total) VALUES (?, ?, ?, ?, ?)";
            insert = conn.prepareStatement(sql2);
            
            String product_id = "";
            String price = "";
            String qty = "";
            int total = 0;
            
            
            for(int i = 0; i<jTable1.getRowCount(); i++){
            
                product_id = (String)jTable1.getValueAt(i, 0);
                price = (String)jTable1.getValueAt(i, 2);
                qty = (String)jTable1.getValueAt(i, 3);
                total = (int)jTable1.getValueAt(i, 4);
                
                insert.setInt(1, lastinsertid);
                insert.setString(2, product_id);
                insert.setString(3, price);
                insert.setString(4, qty);
                insert.setInt(5, total);
                insert.executeUpdate();
                
            }
            
            String sql3 = "Update product set Qty = Qty-? where Barcode = ?";
            insert = conn.prepareStatement(sql3);
            
            for(int i = 0; i<jTable1.getRowCount(); i++){
            
                product_id = (String)jTable1.getValueAt(i, 0);
                qty = (String)jTable1.getValueAt(i, 3);
                
                insert.setString(1, qty);
                insert.setString(2, product_id);
                insert.executeUpdate();
                
            }
            
            insert.addBatch();
//            JOptionPane.showMessageDialog(this, "Record Saved!!");
            jLabelSuccess.setText("Record Saved!!");
            
        } catch (SQLException ex) {
            Logger.getLogger(POS.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }
    
    
